numeroUno = float(input('Introducir Número:'))
numeroDos = float(input('Introducir Número:'))
numeroTres = float(input('Introducir Número:'))

promedio = (numeroUno + numeroDos + numeroTres) / 3

if numeroUno > promedio:
    print(f'El número {numeroUno} es mayor que promedio')
if numeroDos > promedio:
    print(f'El número {numeroDos} es mayor que promedio')
if numeroTres > promedio:
    print(f'El número {numeroTres} es mayor que promedio')
